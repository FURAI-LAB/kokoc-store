export function normalizeLocale(locale) {
  return SUPPORTED_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;
}

export function getLocaleFromRequest(request) {
  const url = new URL(request.url);
  const queryLocale = normalizeLocale(url.searchParams.get("lang"));
  if (SUPPORTED_LOCALES.includes(url.searchParams.get("lang"))) {
    return { locale: queryLocale, shouldSetCookie: true };
  }

  const cookieHeader = request.headers.get("cookie") || "";
  const cookieLocale = cookieHeader
    .split(";")
    .map(part => part.trim())
    .find(part => part.startsWith(`${LOCALE_COOKIE}=`))
    ?.split("=")[1];

  if (cookieLocale && SUPPORTED_LOCALES.includes(cookieLocale)) {
    return { locale: cookieLocale, shouldSetCookie: false };
  }

  // No cookie — detect by Cloudflare country
  const country = request.cf?.country ?? "";
  const geoLocale = country === "RU" ? "ru" : "en";
  return { locale: geoLocale, shouldSetCookie: true };
}

export function localeHeaders({ locale, shouldSetCookie } = {}) {
  const headers = { vary: "Cookie" };
  if (shouldSetCookie) {
    headers["Set-Cookie"] = `${LOCALE_COOKIE}=${normalizeLocale(locale)}; Path=/; Max-Age=31536000; SameSite=Lax`;
  }
  return headers;
}

export function i18n(locale = DEFAULT_LOCALE) {
  const safeLocale = normalizeLocale(locale);
  const dict = translations[safeLocale];
  return {
    locale: safeLocale,
    t(key) {
      return dict[key] ?? translations[DEFAULT_LOCALE][key] ?? key;
    }
  };
}

/**
 * Generic RU/EN pluralization. `keys` = { one, few, many } i18n key names.
 * pluralItems() below is the original item-count specific wrapper.
 */
export function pluralize(count, tr, keys) {
  if (tr.locale === "en") return `${count} ${count === 1 ? tr.t(keys.one) : tr.t(keys.many)}`;

  const mod10 = count % 10;
  const mod100 = count % 100;
  const key = mod10 === 1 && mod100 !== 11
    ? keys.one
    : mod10 >= 2 && mod10 <= 4 && (mod100 < 12 || mod100 > 14)
      ? keys.few
      : keys.many;

  return `${count} ${tr.t(key)}`;
}

export function pluralItems(count, tr) {
  return pluralize(count, tr, { one: "itemOne", few: "itemFew", many: "itemMany" });
}

